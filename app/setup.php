//redirige a la página de agradecimiento después de comprar
add_filter('template_include', function ($template) {
    if (is_order_received_page()) {
        $order_id = absint(get_query_var('order-received'));
        $order = wc_get_order($order_id);

        if ($order) {
            echo \Roots\view('woocommerce.thankyou', [
                'order' => $order,
            ])->render();
            exit;
        }
    }

    return $template;
}, 99);

//página de ordenes
add_filter('template_include', function ($template) {
    if (is_wc_endpoint_url('orders')) {
        $blade_template = locate_template('resources/views/woocommerce/myaccount/orders.blade.php');

        if ($blade_template) {
            $current_page = max(1, get_query_var('paged'));
            $customer_orders = wc_get_orders([
                'customer_id' => get_current_user_id(),
                'paginate' => true,
                'paged' => $current_page,
                'limit' => 10,
            ]);

            echo \Roots\view('woocommerce.myaccount.orders', [
                'orders' => $customer_orders,
                'current_page' => $current_page,
            ])->render();
            exit;
        }
    }

    return $template;
}, 99);

/**
 * Register the theme sidebars.
 *
 * @return void
 */
add_action('widgets_init', function () {
    $config = [
        'before_widget' => '<section class="widget %1$s %2$s">',
        'after_widget' => '</section>',
        'before_title' => '<h3>',
        'after_title' => '</h3>',
    ];

    register_sidebar([
        'name' => __('Primary', 'sage'),
        'id' => 'sidebar-primary',
    ] + $config);

    register_sidebar([
        'name' => __('Footer', 'sage'),
        'id' => 'sidebar-footer',
    ] + $config);
});


